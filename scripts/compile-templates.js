const fs = require("fs");
const path = require("path");
const swc = require("@swc/core");

const rootDir = path.join(__dirname, "..");
const templatesDir = path.join(rootDir, "templates");

function findTypeScriptFiles(dir) {
  const results = [];
  const files = fs.readdirSync(dir);

  for (const file of files) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);

    if (stat.isDirectory()) {
      results.push(...findTypeScriptFiles(filePath));
    } else if (file.endsWith(".ts") && !file.endsWith(".d.ts")) {
      results.push(filePath);
    }
  }

  return results;
}

// Only strip `.ts` from imports that resolve to a sibling file we also
// compile (inside templates/) — those get a real .js file to `require()`.
// Imports that escape outside templates/ (e.g. the db/client.ts shim's
// "../../../lib/db/client.ts") must KEEP their .ts extension: that file is
// never compiled by this script, and per CLAUDE.md it's designed to be loaded
// by Node's own native TypeScript type-stripping, which only activates when
// the extension is explicit in the specifier.
function stripTsExtensions(code, fileDir) {
  const rewrite = (specifier) => {
    if (!specifier.endsWith(".ts") || !specifier.startsWith(".")) return specifier;
    const resolved = path.resolve(fileDir, specifier);
    const isInsideTemplates = resolved.startsWith(templatesDir + path.sep);
    return isInsideTemplates ? specifier.slice(0, -3) : specifier;
  };

  return code
    .replace(/from\s+["']([^"']+)["']/g, (m, spec) => `from "${rewrite(spec)}"`)
    .replace(/require\((["'])([^"']+)\1\)/g, (m, q, spec) => `require(${q}${rewrite(spec)}${q})`);
}

try {
  const tsFiles = findTypeScriptFiles(templatesDir);
  if (tsFiles.length === 0) {
    console.log("✓ No TypeScript template files to compile");
    process.exit(0);
  }

  console.log(`Compiling ${tsFiles.length} template TypeScript files with SWC...`);

  let successCount = 0;
  for (const file of tsFiles) {
    try {
      const code = fs.readFileSync(file, "utf-8");
      const result = swc.transformSync(code, {
        jsc: {
          parser: {
            syntax: "typescript",
          },
          target: "es2020",
        },
        // CommonJS output so plain .js works under Node's default module
        // system — the deployed package.json has no "type": "module".
        module: {
          type: "commonjs",
        },
      });

      // Fix .ts extension imports
      const fixedCode = stripTsExtensions(result.code, path.dirname(file));
      const banner = `// AUTO-GENERATED by scripts/compile-templates.js from ${path.basename(file)} — do not edit, edit the .ts file instead.\n`;

      const outFile = file.replace(/\.ts$/, ".js");
      fs.writeFileSync(outFile, banner + fixedCode, "utf-8");
      console.log(`  ✓ ${path.relative(rootDir, file)}`);
      successCount++;
    } catch (err) {
      console.error(`  ✗ Failed to compile ${path.relative(rootDir, file)}: ${err.message}`);
      throw err;
    }
  }

  console.log(`✓ Template compilation complete (${successCount}/${tsFiles.length})`);
} catch (error) {
  console.error("✗ Template compilation failed:", error.message);
  process.exit(1);
}
